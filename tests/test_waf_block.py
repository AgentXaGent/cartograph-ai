@respx.mock
def test_probe_blocked_passes_strict_mode():
    """probe_blocked is a definitive, evidence-backed classification.

    Unlike probe_unreachable (0.0 confidence, nothing learned), a
    vendor-identified block is actionable intelligence; strict mode
    returns it rather than raising.
    """
    _mock_blocked_site(
        "https://filingaccess.serff.com/",
        headers={"server": "awselb/2.0"},
        body="",
    )

    client = StubAnthropic(StubMessage(content=[StubTextBlock(text="{}")]))
    http_client = httpx.Client(follow_redirects=True, timeout=5.0)
    try:
        result = probe(
            "https://filingaccess.serff.com/",
            anthropic_client=client,
            http_client=http_client,
            options=ProbeOptions(
                strict=True, preflight_key_check=False, polite_delay=0.0
            ),
        )
    finally:
        http_client.close()

    assert result.classification.category == "probe_blocked"
    assert result.classification.subcategory == "aws_waf_elb"
